#include "dialogs.h"

#include <gtkmm/messagedialog.h>

namespace Quafe {

void show_error_dialog(ustring primary, ustring secondary) {
	Gtk::MessageDialog dialog(primary, false, Gtk::MESSAGE_ERROR);
	dialog.set_secondary_text(secondary);
	dialog.run();
}
}

